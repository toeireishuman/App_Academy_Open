/*
We have provided a function named isCool. The isCool function
will take a string argument and console.log and return a sentence
saying that the given string "is cool!". Call the isCool function
with the arguments listed below.
*/

function isCool(string) {
	let coolSentence = string + " is cool!";
	console.log(coolSentence);
	return coolSentence;
}

// Call the above function passing in the below arguments:
// Music
// JavaScript
// The world

isCool("Music");
isCool("Javascript");
isCool("The world");
